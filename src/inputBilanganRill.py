from __future__ import print_function

def main():
    bilanganPertama = float(input('Bilangan Pertama : '))
    bilanganKedua = float(input('Bilangan Kedua : '))
    hasil = bilanganPertama * bilanganKedua
    print('Bilangan Pertama : %f'%bilanganPertama)
    print('Bilangan Kedua : %f'%bilanganKedua)
    print('Hasil : %f'%hasil)
if __name__ == '__main__':
    main()